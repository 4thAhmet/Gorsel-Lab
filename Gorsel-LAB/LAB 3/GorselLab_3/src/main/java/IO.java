    private int satırGetir(long no) {
        for (int i = 0; i<tableModel.getRowCount(); i++){
            if ((int) tableModel.getValueAt(i, 0) == no){
                return i;
            }
        }
        return -1;
    }
    private void sayac(){
        for (int i = 0; i<10; i++){
            try {
                Thread.sleep(50);
            } catch (InterruptedException ex) {
                Logger.getLogger(NO.class.getName()).log(Level.SEVERE, null, ex);
            }
            jProgressBar1.setValue(jProgressBar1.getValue()+1);
            jProgressBar1.setString("%"+jProgressBar1.getValue());
            jProgressBar1.update(jProgressBar1.getGraphics());
        }
        jProgressBar1.setValue(0);
    }
