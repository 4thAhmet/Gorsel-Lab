    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String password = "123";
        
        long gonderenNo = (long) jFormattedTextField2.getValue();
        long aliciNo = (long) jFormattedTextField3.getValue();

        if (password.equals(new String(jPasswordField1.getPassword())) ) {
            
            sayac();
            int gonderenSatir = satırGetir(gonderenNo), aliciSatir = satırGetir(aliciNo);
            if (gonderenSatir != -1 && aliciSatir != -1) {
                
                int miktar = (int) ((long) jFormattedTextField1.getValue());
                
                int gonderenBakiye = (int) tableModel.getValueAt(gonderenSatir, 1);
                int aliciBakiye =  (int) tableModel.getValueAt(aliciSatir, 1);
                
                tableModel.setValueAt(gonderenBakiye-miktar, gonderenSatir, 1);
                tableModel.setValueAt(aliciBakiye+miktar, aliciSatir, 1);
            }
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

